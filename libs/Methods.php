    public function newDate()
    {
        return date('d-m-Y H:m:s');
    }

    public function newDateDb()
    {
        return date('Y-m-d H:m:s');
    }

    public function calcDesconto($valor_unidade, $porcentagem)
    {
        return $valor_unidade - (($porcentagem / 100) * $valor_unidade);
    }
    public function abreviaTexto($texto, $limite){
        $texto = substr($texto, 0, strrpos(substr($texto, 0, $limite), ' ')) . '...';
        return $texto;
    }

    public function toLower($texto) {
        return mb_convert_case($texto, MB_CASE_LOWER, 'UTF-8');
    }

    public function toUpper($texto) {
        return mb_convert_case($texto, MB_CASE_UPPER, 'UTF-8');
    }

    public function toCase($texto) {
        return mb_convert_case($texto, MB_CASE_TITLE, 'UTF-8');
    }

    public function telefone($valor){
        $valor = trim($valor);
        $valor = str_replace(" ", "", $valor);
        $valor = str_replace("-", "", $valor);
        return $valor;
    }

